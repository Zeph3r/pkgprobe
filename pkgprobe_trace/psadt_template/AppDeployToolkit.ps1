# ---------------------------------------------------------------------------
# Write-Log
# ---------------------------------------------------------------------------
function Write-Log {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)][string]$Message,
        [ValidateSet('Info','Warning','Error')][string]$Severity = 'Info'
    )
    $ts = Get-Date -Format 'yyyy-MM-dd HH:mm:ss'
    $line = "$ts [$Severity] $Message"
    Write-Host $line
    if ($script:LogFile) {
        try { $line | Out-File -FilePath $script:LogFile -Append -Encoding utf8 } catch {}
    }
}

# ---------------------------------------------------------------------------
# Initialize-Logging
# ---------------------------------------------------------------------------
function Initialize-Logging {
    [CmdletBinding()]
    param([string]$AppName = 'PkgProbeWrap')
    $logDir = Join-Path $env:windir 'Logs\Software'
    if (-not (Test-Path $logDir)) {
        try { New-Item -Path $logDir -ItemType Directory -Force | Out-Null } catch {}
    }
    $script:LogFile = Join-Path $logDir "$AppName`_Deploy.log"
    Write-Log "=== pkgprobe PSADT wrapper started ==="
}

# ---------------------------------------------------------------------------
# Show-InstallationWelcome
# ---------------------------------------------------------------------------
function Show-InstallationWelcome {
    [CmdletBinding()]
    param(
        [string]$CloseApps = '',
        [switch]$Silent,
        [int]$CloseAppsCountdown = 60,
        [switch]$ForceCloseAppsCountdown,
        [switch]$CheckDiskSpace,
        [switch]$PersistPrompt,
        [int]$DeferTimes = 0,
        [int]$DeferDays = 0,
        [switch]$BlockExecution
    )
    if (-not $CloseApps) { return }
    $apps = $CloseApps -split ','
    foreach ($app in $apps) {
        $app = $app.Trim()
        if (-not $app) { continue }
        $procs = Get-Process -Name $app -ErrorAction SilentlyContinue
        if ($procs) {
            Write-Log "Closing process: $app ($($procs.Count) instance(s))"
            $procs | ForEach-Object {
                try { $_.CloseMainWindow() | Out-Null } catch {}
            }
            Start-Sleep -Seconds 2
            $remaining = Get-Process -Name $app -ErrorAction SilentlyContinue
            if ($remaining) {
                Write-Log "Force-stopping: $app"
                $remaining | Stop-Process -Force -ErrorAction SilentlyContinue
            }
        }
    }
}

