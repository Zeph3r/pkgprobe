# ---------------------------------------------------------------------------
# Execute-Process
# ---------------------------------------------------------------------------
function Execute-Process {
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)][string]$Path,
        [string]$Parameters = '',
        [switch]$WaitForMsiExec,
        [int[]]$IgnoreExitCodes = @(),
        [switch]$PassThru,
        [switch]$NoWait
    )
    Write-Log "Execute-Process: $Path $Parameters"

    $psi = New-Object System.Diagnostics.ProcessStartInfo
    $psi.FileName  = $Path
    $psi.Arguments = $Parameters
    $psi.UseShellExecute = $false
    $psi.RedirectStandardOutput = $true
    $psi.RedirectStandardError  = $true

    $proc = [System.Diagnostics.Process]::Start($psi)

    if (-not $NoWait) {
        $proc.WaitForExit()
        $ec = $proc.ExitCode
        $stdout = $proc.StandardOutput.ReadToEnd()
        $stderr = $proc.StandardError.ReadToEnd()

        if ($stdout) { Write-Log "STDOUT: $($stdout.Substring(0, [Math]::Min(2000, $stdout.Length)))" }
        if ($stderr) { Write-Log "STDERR: $($stderr.Substring(0, [Math]::Min(2000, $stderr.Length)))" -Severity Warning }
        Write-Log "Exit code: $ec"

        if ($ec -ne 0 -and $IgnoreExitCodes -notcontains $ec) {
            if ($ec -eq 3010) {
                Write-Log 'Exit code 3010: reboot required.' -Severity Warning
            } elseif ($ec -eq 1641) {
                Write-Log 'Exit code 1641: reboot initiated by installer.' -Severity Warning
            } else {
                Write-Log "Non-zero exit code: $ec" -Severity Error
            }
        }

        if ($PassThru) { return $proc }
        return $ec
    }

    if ($PassThru) { return $proc }
}

